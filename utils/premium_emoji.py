_TAG_RE = re.compile(r"<tg-emoji\b[^>]*>(.*?)</tg-emoji>", re.S)


def ce(name: str) -> str:
    """HTML премиум-эмодзи по имени концепта.

    Если id пуст или концепт неизвестен — возвращает обычный эмодзи (alt),
    поэтому сообщение остаётся корректным и красивым ещё до заполнения id.
    """
    e = E.get(name)
    if not e:
        return ""
    alt = e.get("alt", "")
    eid = e.get("id") or ""
    if not eid:
        return alt
    return f'<tg-emoji emoji-id="{eid}">{alt}</tg-emoji>'


def has_premium(name: str) -> bool:
    """True, если для концепта вписан реальный custom_emoji_id."""
    e = E.get(name)
    return bool(e and e.get("id"))


def strip_custom_emoji(html: str) -> str:
    """Заменяет <tg-emoji>…</tg-emoji> на их обычный эмодзи (путь фолбэка)."""
    return _TAG_RE.sub(r"\1", html)
